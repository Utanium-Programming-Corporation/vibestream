  static List<TitleEntity> searchTitles(String query) {
    final lowerQuery = query.toLowerCase();
    return mockTitles.where((t) => 
      t.title.toLowerCase().contains(lowerQuery) ||
      t.genres.any((g) => g.toLowerCase().contains(lowerQuery)) ||
      t.vibeTags.any((v) => v.toLowerCase().contains(lowerQuery))
    ).toList();
  }

  static List<TitleEntity> getTitlesByType(TitleType type) =>
      mockTitles.where((t) => t.type == type).toList();

  static List<TitleEntity> getTrendingTitles() =>
      mockTitles.take(6).toList();

  static List<TitleEntity> getRecommendationsForMood(String mood) {
    final moodLower = mood.toLowerCase();
    return mockTitles.where((t) =>
      t.vibeTags.any((v) => v.toLowerCase().contains(moodLower)) ||
      t.vibeExplanation?.toLowerCase().contains(moodLower) == true
    ).toList();
  }
